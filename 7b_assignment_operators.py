# Assignment operators are used to assign values to variables:

# "=" operator
x = 5

print(x)

# "+=" operator
x = 5

x += 3 # same as x = x + 3

print(x)

# "-=" operator

x = 5

x -= 3 # same as x = x - 3

print(x)

#  "*= " operator

x = 5

x *= 3 # same as 	x = x * 3

print(x)


# /= operator

x = 5

x /= 3 # same as x = x / 3

print(x)


# %= operator

x = 5

x%=3 # same as 	x = x % 3

print(x)

# //= operator

x = 5

x//=3 # same as x = x // 3

print(x)

# **= operator

x = 5

x **= 3 # same as 	x = x ** 3

print(x)

# &= operators

x = 5

x &= 3 # same as x = x & 3

print(x)

# |= operators

x = 5

x |= 3 # same as 	x = x | 3

print(x)

# ^= operators

x = 5

x ^= 3 # same as 	x = x ^ 3

print(x)

# >>= operators

x = 5

x >>= 3 # same as 	x = x >> 3

print(x)

# <<= perators

x = 5

x <<= 3 # same as 	x = x << 3

print(x)